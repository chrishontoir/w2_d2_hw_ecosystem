class Bears

  def initialize(bear_name, bear_type, stomach_array)
    @bear_name = bear_name
    @bear_type = bear_type
    @stomach_array = stomach_array
  end

  def get_bear_name
    return @bear_name
  end

  def get_bear_type
    return @bear_type
  end

  def get_stomach_array
    return @stomach_array
  end


end
